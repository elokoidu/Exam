     * Store a newly created resource in storage.
     *
     * @param \Illuminate\Http\Request $request
     * @return \Illuminate\Http\Response
     * @throws \Illuminate\Validation\ValidationException
     */
    public function store(Request $request)
    {
        $this->validate($request, array(
           'nimi' => 'required',
           'hind' => 'required',
           'tootekood' => 'required',
           'tootefoto' => 'required',
           'tootja' => 'required',
           'platvorm' => 'required',
        ));
        $post = new Post;
        $post->nimi = $request->nimi;
        $post->hind = $request->hind;
        $post->tootekood = $request->tootekood;
        $post->tootefoto = $request->tootefoto;
        $post->tootja = $request->tootja;
        $post->platvorm = $request->platvorm;

        $post->save();

        return redirect()->route('posts.show', ['id' => $post->id]);

    }

